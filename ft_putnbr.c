#include "libft.h"

void	ft_putnbr(int n)
{
	if (n >= 0)
		n = n * (-1);
	else
		ft_putchar('-');
	if (n < -9)
		ft_putnbr(-(n / 10));
	ft_putchar(-(n % 10) + 48);
}
